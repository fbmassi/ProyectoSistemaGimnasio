    private void botonMostrarTiposDeColchonetasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMostrarTiposDeColchonetasActionPerformed
        String devolver = "";
        for (Articulo art: soporteTécnico.getArticulos()){
            if (art instanceof Colchoneta) {
                Colchoneta colch = (Colchoneta) art;
                devolver += "<p>ARTICULO: " + colch.getTipo() + " - " + colch.getMarca()
                + " - " + colch.getTipoAmortizacion() + " - " + colch.getDuracion()
                + " -  DIMENSIONES:  " + colch.getLargo() + "mts de largo X " + colch.getAncho() + "mts de ancho.</p>";
            }
        }
        resultadosDeBusqueda.setText(strHTML(devolver));
    }//GEN-LAST:event_botonMostrarTiposDeColchonetasActionPerformed


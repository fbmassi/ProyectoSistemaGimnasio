    private void botonCrearLoteColchonetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearLoteColchonetaActionPerformed
        String fabricante = marca.getText();
        String tipo_amortizacion = amortizacion.getText().toUpperCase();
        String fecha = fechaDeCreacion.getText();
        String anchoSTR = ancho.getText().toUpperCase();
        String largoSTR = largo.getText();
        String cantidadSTR = cantidad.getText();
        try {
            if (!tipo_amortizacion.equals("POR USO") && !tipo_amortizacion.equals("POR FECHA")) {
                IngresoErroneo ingresoErroneo = new IngresoErroneo();
                ingresoErroneo.setVisible(true);
                ingresoErroneo.setLocationRelativeTo(null);
            } else {
                //admin.agregarArticulos("Recoleta", "PESA", "Gadnic", "Por uso", "2023-12-06", "50", "3", "De tobillera", null, null, null);
                admin.agregarArticulos(sedeSeleccionada, "COLCHONETA", fabricante, tipo_amortizacion, fecha, cantidadSTR, null, null, largoSTR, anchoSTR, null);
            }
        } catch (DateTimeParseException e) {
            IngresoErroneo ingresoErroneo = new IngresoErroneo();
            ingresoErroneo.setVisible(true);
            ingresoErroneo.setLocationRelativeTo(null);
        }
        marca.setText("");
        amortizacion.setText("");
        ancho.setText("");
        largo.setText("");
        cantidad.setText("");
        fechaDeCreacion.setText("");
    }//GEN-LAST:event_botonCrearLoteColchonetaActionPerformed

